public class TipoAbono {
    
    private int id;
    private String nome;
    private String especie_abono;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEspecie_abono() {
        return especie_abono;
    }

    public void setEspecie_abono(String especie_abono) {
        this.especie_abono = especie_abono;
    }
    
    
  
    
}
